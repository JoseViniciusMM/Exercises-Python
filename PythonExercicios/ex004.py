'''
4. Leia um número real e imprima o resultado do quadrado desse número.
'''
numero = float (input('Digite um número...:'))
d = numero*2
t = numero*3
q = numero*4
r = numero**(1/2)
print(f'O dobro do número {numero} é {d}')
print(f'O triplo do número {numero} é {t}')
print(f'O quadrado do número {numero} é {q}')
print(f'A raiz quadrada do {numero} é {r}')
