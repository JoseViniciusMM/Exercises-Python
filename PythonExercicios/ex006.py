'''
6. Leia uma temperatura em graus Celcius e apresente-a convertida em graus Fahrenheit.
A fórmula de conversão é: F = C*(9.0/5.0)+32.0, sendo F a temperatira em Fahrenheit e
C a temperatura em Celcius.
'''

C = float(input('Digite uma Temperatura em Celcius...:'))
F = C*(9.0/5.0)+32.0
print(f'A temperatura {C}° graus Celcius é igual a {F}° graus Fahrenheit')
